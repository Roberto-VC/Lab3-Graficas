def update(screen, cur, size):
    next = zeros((cur.shape[0], cur.shape[1]))

    for r, c in ndindex(cur.shape):
        alive = sum(cur[r-1:r+2, c-1:c+2]) - cur[r,c]
        col = (0,0,0) if cur[r,c] == 0 else (1,1,1)
        if cur[r,c] == 1:
            if alive < 2 or alive > 3:
                col = (100,100,100)
            elif 2 <= alive <= 3:
                col = (255,255,255)
                next[r,c] = 1
                
        else:
            if alive == 3:
                next[r,c] = 1
            
        pygame.draw.rect(screen, col, (c*size, r*size,size-1,size-1))

    return next

